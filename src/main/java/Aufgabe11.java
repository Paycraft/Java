public class Aufgabe11 {
    public static void main(String[] args) {
        System.out.println(Math.random()); //eine zufällige zahl unter 1 ausgeben
        System.out.println(Math.PI); //pi ausgeben
        System.out.println(Math.sqrt(81)); //wurzel ausgeben
        System.out.println(Math.round(3.14)); //gerundetes Resultat ausgeben
        System.out.println(Math.max(2343, 432)); //die grösste zahl ausgeben
    }
}
